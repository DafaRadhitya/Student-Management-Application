    private void btntambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntambahActionPerformed
        // TODO add your handling code here:
        if (selectedid != 0) {
            JOptionPane.showMessageDialog(this, 
                    "Anda sedang memilih data! klik tombol ubah jika ingin mengubah");
            return;
        }
        String namakelas = txtnamakelas.getText().trim();
        
        if (namakelas.isEmpty()) {
            JOptionPane.showMessageDialog(this, 
                    "nama kelas harus diisi");
            return;
        }
        String sql = "INSERT INTO class" 
                + "(Class_Name) VALUES (?)";
        PreparedStatement ps = null;
        try {
            ps = conn.prepareStatement(sql);
            ps.setString(1, namakelas);
            
            int hasil = ps.executeUpdate();
            if (hasil > 0){
                JOptionPane.showMessageDialog(this, "data kelas berhasil ditambah");
                
                txtnamakelas.setText("");
         
                tampiltabel();
            } else{
                JOptionPane.showMessageDialog(this, 
                        "gagal menambahkan data kelas");
            }
        } catch (SQLException ex){
            JOptionPane.showMessageDialog(this, "error insert: " + ex.getMessage());
        }finally {
            try {
                if (ps != null) ps.close();
            } 
            catch (SQLException e){
                
            }
        }
    }//GEN-LAST:event_btntambahActionPerformed

    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        // TODO add your handling code here:
        String namakelas = txtnamakelas.getText().trim();
        
        if (selectedid == 0) {
            JOptionPane.showMessageDialog(this, "Pilih data kelas dari tabel terlebih dahulu!");
            return;
        }
        
        String sql = "UPDATE class SET Class_Name = ? WHERE ID = ?";
        PreparedStatement ps = null;
        try {
            ps = conn.prepareStatement(sql);
            ps.setString(1, namakelas);
            ps.setInt(2, selectedid);
            
            int hasil = ps.executeUpdate();
            if (hasil > 0){
                JOptionPane.showMessageDialog(this, "data kelas berhasil diubah");
                
                txtnamakelas.setText("");
                selectedid = 0;
                
                tampiltabel();
            } else{
                JOptionPane.showMessageDialog(this, 
                        "gagal mengubah data kelas");
            }
        } catch (SQLException ex){
            JOptionPane.showMessageDialog(this, "error insert: " + ex.getMessage());
        }finally {
            try {
                if (ps != null) ps.close();
            } 
            catch (SQLException e){
                
            }
        }
    }//GEN-LAST:event_btnupdateActionPerformed

    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        // TODO add your handling code here:
        if (selectedid == 0) {
            JOptionPane.showMessageDialog(this, "Pilih data kelas dari tabel terlebih dahulu!");
            return;
        }
        int confirm = JOptionPane.showConfirmDialog(
                this,
                "Yakin ingin menghapus data kelas ini?",
                "Konfirmasi Hapus",
                JOptionPane.YES_NO_OPTION
        );

        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }
        String sql = "DELETE FROM class WHERE ID = ?";
        PreparedStatement ps = null;
        try {
            ps = conn.prepareStatement(sql);
            ps.setInt(1, selectedid);

            int hasil = ps.executeUpdate();

            if (hasil > 0) {
                JOptionPane.showMessageDialog(this, 
                        "Data kelas berhasil dihapus");

                txtnamakelas.setText("");
                selectedid = 0;

                tampiltabel();
            } else {
                JOptionPane.showMessageDialog(this, 
                        "Gagal menghapus data kelas");
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, 
                    "Error delete: " + ex.getMessage());
        } finally {
            try {
                if (ps != null) ps.close();
            } catch (SQLException e) {
            }
        }
    }//GEN-LAST:event_btndeleteActionPerformed

