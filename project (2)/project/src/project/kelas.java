    private DefaultTableModel buattabelmodel (ResultSet rs) throws SQLException {
        dtm = new DefaultTableModel();
        rsmd = rs.getMetaData();
        
        int jmlkolom = rsmd.getColumnCount();
        
        String [] namaKolom = new String[jmlkolom];
        for (int i = 0; i < jmlkolom; i++) {
            namaKolom[i] = rsmd.getColumnName(i + 1);
            } 
        dtm.setColumnIdentifiers(namaKolom);
            
            while (rs.next()) {
                String[] users = new String[jmlkolom];
                for (int i = 0; i < jmlkolom; i++) {
                    users [i] = rs.getString(i + 1);
                }
                dtm.addRow(users);
        }
            return dtm;
    }
    
    private boolean tampiltabel() {
        String sql = "SELECT ID, Class_Name FROM class";
          try {
              stmt = conn.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE,
                      ResultSet.CONCUR_READ_ONLY);
              rs = stmt.executeQuery(sql);
              
              rs.last();
              if (rs.getRow() == 0){
                  JOptionPane.showMessageDialog(null, "data kelas tidak ada");
              } else {
                  rs.beforeFirst();
                  dtm = buattabelmodel(rs);
                  tblkelas.setModel(dtm);
                  
                  tblkelas.setModel(dtm);
                  tblkelas.getColumnModel().getColumn(0).setMinWidth(0);
                  tblkelas.getColumnModel().getColumn(0).setMaxWidth(0);
                  tblkelas.getColumnModel().getColumn(0).setWidth(0);
                  
                  tblkelas.setAutoCreateRowSorter(true);
              }
              return true;
          }catch (SQLException ex){
              JOptionPane.showMessageDialog(null, "error: " + ex.getMessage());
          }
          return false;   
    }

